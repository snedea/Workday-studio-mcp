import { z } from 'zod';
import { readFile, writeFile, copyFile } from 'fs/promises';
import { join } from 'path';
import { existsSync } from 'fs';
import { XMLParser } from 'fast-xml-parser';
import { config } from '../config.mjs';
import { resolveSafe } from '../fs.mjs';
import { validateAssembly } from '../assembly-validator.mjs';

// ─── Public tool registration ─────────────────────────────────────────────────

export function register(server) {
  server.tool(
    'update_sub_flow',
    [
      'Replaces the <cc:steps> body of a named sub-flow in assembly.xml.',
      'Use this to fill in a TODO stub generated by plan_integration.',
      '',
      'Finds <cc:async-mediation id="Do{sub_flow_id}"> and replaces its <cc:steps>',
      'content with steps_xml. Everything else on the async-mediation is preserved:',
      'routes-to, handle-downstream-errors, and the trailing <cc:send-error>.',
      '',
      'steps_xml format:',
      '  - Provide the step elements that go INSIDE <cc:steps>. Do not include the wrapper.',
      '  - Use \\t for tabs. Steps should sit at 4-tab depth (matching skeleton indent).',
      '  - Example for a RAAS fetch + eval:',
      '      \\t\\t\\t\\t<cc:workday-out-rest id="GetWorkers" routes-response-to="TODO_next"',
      '      \\t\\t\\t\\t  extra-path="@{intsys.reportService.getExtrapath(\'alias\')}"/>',
      '',
      'TOP-LEVEL ELEMENTS WARNING (Studio schema rule confirmed INT095, INT069, INT144):',
      'The following elements CANNOT appear inside steps_xml — they MUST be top-level',
      'elements placed directly inside <cc:assembly>, not inside <cc:steps>:',
      '  - cc:workday-out-rest',
      '  - cc:splitter',
      '  - cc:http-out',
      '  - cc:email-out',
      '  - cc:workday-out-soap',
      'If your sub-flow needs to call any of these, place them as top-level elements in',
      'the assembly and chain to them via routes-to on the async-mediation itself.',
      'The validate_assembly tool will catch this mistake automatically.',
      '',
      'A .bak backup is written before every change.',
      'XML well-formedness is validated before the write is committed.',
      '',
      'To also change the routes-to on the async-mediation itself (e.g. from',
      '"EndGetWorkers" to "Splitter"), use write_integration_file for a full rewrite.',
    ].join('\n'),
    {
      project_name: z.string()
        .describe('Project name, e.g. "INT145_My_Integration"'),
      sub_flow_id: z.string()
        .describe('Base id used in plan_integration, e.g. "GetWorkers" (NOT "DoGetWorkers"). The tool looks for id="Do{sub_flow_id}" in the assembly.'),
      steps_xml: z.string()
        .describe('New step elements for inside <cc:steps>. Omit the <cc:steps> wrapper. Use \\t for tabs at 4-tab depth.'),
    },
    async ({ project_name, sub_flow_id, steps_xml }) => {
      let projectPath;
      try {
        ({ projectRoot: projectPath } = resolveSafe(project_name, ''));
      } catch (e) {
        if (e.code === 'PATH_TRAVERSAL_DETECTED') {
          return errResponse('INVALID_PROJECT_NAME', `Invalid project name: ${project_name}`, 'Project name must not contain path traversal sequences such as ../');
        }
        throw e;
      }
      const assemblyPath = join(projectPath, 'ws', 'WSAR-INF', 'assembly.xml');

      if (!existsSync(assemblyPath)) {
        return errResponse(
          'FILE_NOT_FOUND',
          `assembly.xml not found in project '${project_name}'.`,
          'Run plan_integration first to generate the skeleton.',
        );
      }

      const original = await readFile(assemblyPath, 'utf-8');
      const result = replaceStepsBlock(original, sub_flow_id, steps_xml);

      if (!result.ok) {
        return errResponse(result.code, result.message, result.suggestion);
      }

      const xmlError = validateXmlWellFormedness(result.xml);
      if (xmlError) {
        return errResponse(
          'INVALID_XML',
          `The replacement would produce malformed XML: ${xmlError}`,
          'Check steps_xml for unclosed tags, unescaped & characters (use &amp;), or missing wd: namespace prefixes.',
        );
      }

      await copyFile(assemblyPath, assemblyPath + '.bak');
      await writeFile(assemblyPath, result.xml, 'utf-8');

      // Run Studio-specific validation automatically — surface issues immediately
      const wsDir = join(projectPath, 'ws', 'WSAR-INF');
      const issues = validateAssembly(result.xml, wsDir);
      const errors   = issues.filter(i => i.severity === 'ERROR');
      const warnings = issues.filter(i => i.severity === 'WARNING');

      return {
        content: [{
          type: 'text',
          text: JSON.stringify({
            success: true,
            updated: `Do${sub_flow_id}`,
            backup: 'ws/WSAR-INF/assembly.xml.bak',
            validation: {
              clean: issues.length === 0,
              errors:   errors.length,
              warnings: warnings.length,
              issues: issues.length > 0 ? issues : undefined,
            },
            next_step: errors.length > 0
              ? 'Fix the ERRORs above before opening in Studio.'
              : 'Open assembly-diagram.xml in Studio to verify the diagram renders correctly.',
          }, null, 2),
        }],
      };
    },
  );
}

// ─── Core replacement logic ───────────────────────────────────────────────────

function replaceStepsBlock(xml, subFlowId, newStepsContent) {
  const asyncMedTag = `<cc:async-mediation id="Do${subFlowId}"`;
  const asyncMedStart = xml.indexOf(asyncMedTag);

  if (asyncMedStart === -1) {
    return {
      ok: false,
      code: 'SUB_FLOW_NOT_FOUND',
      message: `No element with id="Do${subFlowId}" found in assembly.xml.`,
      suggestion: `Check that sub_flow_id matches exactly what was passed to plan_integration. Run list_assembly_steps to see available ids.`,
    };
  }

  const stepsOpenTag  = '<cc:steps>';
  const stepsCloseTag = '</cc:steps>';

  const stepsOpenIdx = xml.indexOf(stepsOpenTag, asyncMedStart);
  if (stepsOpenIdx === -1) {
    return {
      ok: false,
      code: 'STEPS_NOT_FOUND',
      message: `No <cc:steps> found inside Do${subFlowId}.`,
      suggestion: 'The assembly.xml may have been manually modified. Check the file with read_integration_file.',
    };
  }

  // Guard: no other async-mediation should appear between our tag and the steps open
  const between = xml.substring(asyncMedStart + asyncMedTag.length, stepsOpenIdx);
  if (between.includes('<cc:async-mediation')) {
    return {
      ok: false,
      code: 'AMBIGUOUS_TARGET',
      message: `Found an unexpected nested async-mediation before <cc:steps> in Do${subFlowId}.`,
      suggestion: 'Use write_integration_file for a full rewrite instead.',
    };
  }

  const stepsCloseIdx = xml.indexOf(stepsCloseTag, stepsOpenIdx + stepsOpenTag.length);
  if (stepsCloseIdx === -1) {
    return {
      ok: false,
      code: 'STEPS_UNCLOSED',
      message: `No </cc:steps> closing tag found after <cc:steps> in Do${subFlowId}.`,
      suggestion: 'assembly.xml may be malformed. Validate with validate_xml_file.',
    };
  }

  // Detect the indentation of <cc:steps> so the closing tag stays aligned
  const lineStart = xml.lastIndexOf('\n', stepsOpenIdx) + 1;
  const stepsIndent = xml.substring(lineStart, stepsOpenIdx);

  const before = xml.substring(0, stepsOpenIdx + stepsOpenTag.length);
  const after  = xml.substring(stepsCloseIdx);  // starts with </cc:steps>

  return {
    ok: true,
    xml: `${before}\n${newStepsContent}\n${stepsIndent}${after}`,
  };
}

// ─── XML validation ───────────────────────────────────────────────────────────

function validateXmlWellFormedness(xml) {
  try {
    const parser = new XMLParser({ ignoreAttributes: false, allowBooleanAttributes: true });
    parser.parse(xml);
    return null;
  } catch (e) {
    return e.message;
  }
}

// ─── helpers ─────────────────────────────────────────────────────────────────

function errResponse(code, message, suggestion) {
  return {
    content: [{
      type: 'text',
      text: JSON.stringify({ error: true, code, message, suggestion }),
    }],
  };
}
